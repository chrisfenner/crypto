func TestCounterMode(t *testing.T) {
	for i, tt := range counterModeTests {
		out := Counter(tt.mac, tt.keyLen, tt.label, tt.context)
		if !bytes.Equal(out, tt.out) {
			t.Errorf("test %d: Key() = %x, want %x", i, out, tt.out)
		}
	}
}

func TestCounterModePanics(t *testing.T) {
	mac := func() hash.Hash { return hmac.New(sha256.New, []byte("key")) }
	panics := func(fn func()) (p bool) {
		defer func() { p = recover() != nil }()
		fn()
		return
	}
	for _, keyLen := range []int{-2147483648, -1, 536870912, 2147483647} {
		if !panics(func() { Counter(mac, keyLen, []byte("label"), []byte("context")) }) {
			t.Errorf("requesting 2^32 bits of key should trigger panic")
		}
	}
}

